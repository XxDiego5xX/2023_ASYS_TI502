    private void drawAxisFw() {
        Graphics2D g = (Graphics2D) plotFw.getGraphics();
      
        g.setColor(new Color(50, 90, 20));
        g.fillRect(0,0, 300, 300);
        
        g.setColor(Color.white);
        
        // draw x axis, from  (XMIN,0) to  (XMAX, 0)  
        g.drawLine(
                coordinateFw.toScreenX(coordinateFw.getXMIN()),
                coordinateFw.toScreenY(0.0f),
                coordinateFw.toScreenX(coordinateFw.getXMAX()),
                coordinateFw.toScreenY(0.0f)
        );
        
        for (float dx = coordinateFw.getXMIN(); dx < coordinateFw.getXMAX(); dx += 1.0) {
            g.drawLine(
                coordinateFw.toScreenX(dx),
                coordinateFw.toScreenY(0),
                coordinateFw.toScreenX(dx),
                coordinateFw.toScreenY(0.5f)
            );
        }
        
        // draw y axis, from  (0,YMIN) to  (0, YMAX)  
        g.drawLine(
                coordinateFw.toScreenX(0.0f),
                coordinateFw.toScreenY(coordinateFw.getYMIN()),
                coordinateFw.toScreenX(0.00f),
                coordinateFw.toScreenY(coordinateFw.getYMAX())
        );
        
        for (float dy = coordinateFw.getYMIN(); dy < coordinateFw.getYMAX(); dy += 1.0) {
            g.drawLine(
                coordinateFw.toScreenX(0),
                coordinateFw.toScreenY(dy),
                coordinateFw.toScreenX(0.5f),
                coordinateFw.toScreenY(dy)
            );
        } 
    }
    
    private void drawFunctionFw() {
        ComplexNumber cn [] = tf1.getFw();
        float x[] = new float [cn.length];
        float y[] = new float [cn.length];
        
        for(int i = 0; i < cn.length; i++ ){
            x[i] = cn[i].getA();
            y[i] = cn[i].getB();
        }
      
        // Draw function points
         Graphics2D g = (Graphics2D) plotFw.getGraphics();
         g.setColor(Color.white);         
         for (int i = 0; i < x.length - 1; i++) {
             g.drawLine(
                coordinateFw.toScreenX(x[i]),
                coordinateFw.toScreenY(y[i]),
                coordinateFw.toScreenX(x[i+1]),
                coordinateFw.toScreenY(y[i+1])
            );
         }
    }
    
